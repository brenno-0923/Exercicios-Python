# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota1 = float(input("informa a nota do 1ª bimetre:"))
nota2 = float(input("informa a nota do 2ª bimetre:"))
nota3 = float(input("informa a nota do 3ª bimetre:"))
nota4 = float(input("informa a nota do 4ª bimetre:"))

media = (nota1 + nota2 + nota3 + nota4) / 4

print("A média desses bimestres é =",media)